package com.kba.entity;

import java.util.Date;

/**
 * 举报信息
 * @author 杨智能
 *时间2019-1-13
 */
public class ReportMessage {

	private String reportMessageId;//举报信息编号
	
	private String liveRoomId;//直播间编号
	
	private String userId;//用户编号
	
	private String reportContent;//举报内容
	
	private Date reportTime;//举报时间
	
	private Integer reportHandleResult;//举报处理结果
	
	private String reportMessageRemark;//举报信息备注
	public String getReportMessageId() {
		return reportMessageId;
	}

	public void setReportMessageId(String reportMessageId) {
		this.reportMessageId = reportMessageId;
	}

	public String getLiveRoomId() {
		return liveRoomId;
	}

	public void setLiveRoomId(String liveRoomId) {
		this.liveRoomId = liveRoomId;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getReportContent() {
		return reportContent;
	}

	public void setReportContent(String reportContent) {
		this.reportContent = reportContent;
	}

	public Date getReportTime() {
		return reportTime;
	}

	public void setReportTime(Date reportTime) {
		this.reportTime = reportTime;
	}

	public Integer getReportHandleResult() {
		return reportHandleResult;
	}

	public void setReportHandleResult(Integer reportHandleResult) {
		this.reportHandleResult = reportHandleResult;
	}

	public String getReportMessageRemark() {
		return reportMessageRemark;
	}

	public void setReportMessageRemark(String reportMessageRemark) {
		this.reportMessageRemark = reportMessageRemark;
	}

	@Override
	public String toString() {
		return "举报信息 [举报信息编号=" + reportMessageId + ", 直播间编号=" + liveRoomId + ", 用户编号=" + userId
				+ ", 举报内容=" + reportContent + ", 举报时间=" + reportTime + ", 举报处理结果="
				+ reportHandleResult + ", 举报信息备注=" + reportMessageRemark + "]";
	}
	
	
}
