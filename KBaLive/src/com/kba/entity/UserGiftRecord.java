package com.kba.entity;

import java.util.Date;

public class UserGiftRecord {

	private String userGiftId;//用户赠礼编号
	private String liveRoomId;//直播间编号
	private String userId;//用户编号
	private String giftTypeId;//礼物类型编号
	private int giftNumber;//赠送礼物数量
	private Date giftTime;//赠礼时间
	private String anchorUserId;//主播用户编号
	private String userGiftRemark;//用户赠礼备注
	@Override
	public String toString(){
		return "用户赠礼记录 [用户赠礼编号=" + userGiftId + ",直播间编号=" + liveRoomId + ",用户编号=" + userId 
				+ ",礼物类型编号=" + giftTypeId + ",赠送礼物数量=" + ",赠礼时间=" + giftTime + ",直播用户编号=" + anchorUserId + ",用户赠礼备注=" + userGiftRemark + "]";
	}
	public String getUserGiftId() {
		return userGiftId;
	}
	public void setUserGiftId(String userGiftId) {
		this.userGiftId = userGiftId;
	}
	public String getLiveRoomId() {
		return liveRoomId;
	}
	public void setLiveRoomId(String liveRoomId) {
		this.liveRoomId = liveRoomId;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getGiftTypeId() {
		return giftTypeId;
	}
	public void setGiftTypeId(String giftTypeId) {
		this.giftTypeId = giftTypeId;
	}
	public int getGiftNumber() {
		return giftNumber;
	}
	public void setGiftNumber(int giftNumber) {
		this.giftNumber = giftNumber;
	}
	public Date getGiftTime() {
		return giftTime;
	}
	public void setGiftTime(Date giftTime) {
		this.giftTime = giftTime;
	}
	public String getAnchorUserId() {
		return anchorUserId;
	}
	public void setAnchorUserId(String anchorUserId) {
		this.anchorUserId = anchorUserId;
	}
	public String getUserGiftRemark() {
		return userGiftRemark;
	}
	public void setUserGiftRemark(String userGiftRemark) {
		this.userGiftRemark = userGiftRemark;
	}
}
