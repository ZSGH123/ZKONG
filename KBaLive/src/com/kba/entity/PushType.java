package com.kba.entity;
/**
 * 推送类型
 * @author 钟强
 * 创建时间：2019-01-13
 * 修改时间：
 */
public class PushType {
//	推送类型编号	push_type_id	varchar2(5)
//	推送类型名称	push_name	varchar2(30)
//	推送价格	push_price	number
//	推送类型备注	push_type_remark	varchar2(200)
	private String pushTypeId;//	推送类型编号
	private String pushName;//	推送类型名称
	private double pushPrice;//	推送价格
	private String pushTypeRemark;//	推送类型备注
	@Override
	public String toString() {
		return "推送类型 [推送类型编号=" + pushTypeId + ", 推送类型名称=" + pushName + ", 推送价格=" + pushPrice
				+ ", 推送类型备注=" + pushTypeRemark + "]";
	}
	public String getPushTypeId() {
		return pushTypeId;
	}
	public void setPushTypeId(String pushTypeId) {
		this.pushTypeId = pushTypeId;
	}
	public String getPushName() {
		return pushName;
	}
	public void setPushName(String pushName) {
		this.pushName = pushName;
	}
	public double getPushPrice() {
		return pushPrice;
	}
	public void setPushPrice(double pushPrice) {
		this.pushPrice = pushPrice;
	}
	public String getPushTypeRemark() {
		return pushTypeRemark;
	}
	public void setPushTypeRemark(String pushTypeRemark) {
		this.pushTypeRemark = pushTypeRemark;
	}
	

}
