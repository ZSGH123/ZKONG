package com.kba.entity;

import java.util.Date;

/**
 * 房间管理员
 * @author 单杰
 * 创建时间：2019-01-13
 * 修改时间：
 */

public class RoomManagerInfo {
	private String roomManagerId;//房间管理员编号
	private String userId;//用户编号
	private String liveRoomId;//直播间编号
	private Date roomManagerWorkTime;//房管任职时间
	private String roomManagerRemark;//房管信息备注
	
	public String toString(){
		return "房间管理员 [房间管理员编号=" + roomManagerId + ",用户编号=" + userId + ",直播间编号=" + liveRoomId + ",房管任职时间=" + roomManagerWorkTime + ",房管信息备注=" + roomManagerRemark + "]";
	}
	
	public String getRoomManagerId() {
		return roomManagerId;
	}
	public void setRoomManagerId(String roomManagerId) {
		this.roomManagerId = roomManagerId;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getLiveRoomId() {
		return liveRoomId;
	}
	public void setLiveRoomId(String liveRoomId) {
		this.liveRoomId = liveRoomId;
	}
	public Date getRoomManagerWordTime() {
		return roomManagerWorkTime;
	}
	public void setRoomManagerWordTime(Date roomManagerWordTime) {
		this.roomManagerWorkTime = roomManagerWordTime;
	}
	public String getRoomManagerRemark() {
		return roomManagerRemark;
	}
	public void setRoomManagerRemark(String roomManagerRemark) {
		this.roomManagerRemark = roomManagerRemark;
	}
	
	
}
