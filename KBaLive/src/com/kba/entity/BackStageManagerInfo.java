package com.kba.entity;

/**
 * 后台管理员信息表
 * @author 赵科
 * 时间：2019-1-13
 */
public class BackStageManagerInfo {

	private String backStageManagerId;   //后台管理员编号
	private String backStageManagerName;  //后台管理员名称
	private String backStageManagerPassword;  //后台管理密码
	private Integer backStageManagerPosition;  //后台管理员职位
	private String backStageManagerRemark;    //后台管理员备注
	@Override
	public String toString() {
		return "后台管理员信息 [后台管理员编号=" + backStageManagerId + ", 后台管理员名称="
				+ backStageManagerName + ", 后台管理员密码=" + backStageManagerPassword
				+ ", 后台管理员职位=" + backStageManagerPosition + ", 后台管理员备注="
				+ backStageManagerRemark + ", toString()=" + super.toString() + "]";
	}
	public String getBackStageManagerId() {
		return backStageManagerId;
	}
	public void setBackStageManagerId(String backStageManagerId) {
		this.backStageManagerId = backStageManagerId;
	}
	public String getBackStageManagerName() {
		return backStageManagerName;
	}
	public void setBackStageManagerName(String backStageManagerName) {
		this.backStageManagerName = backStageManagerName;
	}
	public String getBackStageManagerPassword() {
		return backStageManagerPassword;
	}
	public void setBackStageManagerPassword(String backStageManagerPassword) {
		this.backStageManagerPassword = backStageManagerPassword;
	}
	public Integer getBackStageManagerPosition() {
		return backStageManagerPosition;
	}
	public void setBackStageManagerPosition(Integer backStageManagerPosition) {
		this.backStageManagerPosition = backStageManagerPosition;
	}
	public String getBackStageManagerRemark() {
		return backStageManagerRemark;
	}
	public void setBackStageManagerRemark(String backStageManagerRemark) {
		this.backStageManagerRemark = backStageManagerRemark;
	}
}
