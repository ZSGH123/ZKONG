package com.kba.entity;


/**
 * 视频类型编号
 * @author 杨智能
 *时间：2019-1-13
 */
public class VideoType {
 
	private String videoTypeId;//视频类型编号
	
	private String videoTypeName;//视频类型名称
	
	private String videoTypeRemark;//视频类型备注
	public String getVideoTypeId() {
		return videoTypeId;
	}

	public void setVideoTypeId(String videoTypeId) {
		this.videoTypeId = videoTypeId;
	}

	public String getVideoTypeName() {
		return videoTypeName;
	}

	public void setVideoTypeName(String videoTypeName) {
		this.videoTypeName = videoTypeName;
	}

	public String getVideoTypeRemark() {
		return videoTypeRemark;
	}

	public void setVideoTypeRemark(String videoTypeRemark) {
		this.videoTypeRemark = videoTypeRemark;
	}

	@Override
	public String toString() {
		return "视频类型 [视频类型编号=" + videoTypeId + ", 视频类型名称=" + videoTypeName + ", 视频类型备注="
				+ videoTypeRemark + "]";
	}
	
	
}
